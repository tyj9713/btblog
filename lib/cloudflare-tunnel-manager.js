const fs = require("node:fs");
const path = require("node:path");
const {
  PROCESS_NAME,
  LOG_FILE,
  buildPortHostname,
  buildTunnelArtifacts,
  publishRemoteIngress,
  resolveTunnelSettings,
} = require("./cloudflare-tunnel-config");

const SHELL_ENV_FILE = "named-tunnel.env";
const STORED_CONFIG_KEYS = [
  "CLOUDFLARE_TUNNEL_TOKEN",
  "CLOUDFLARE_API_TOKEN",
  "CLOUDFLARE_ACCOUNT_ID",
  "CLOUDFLARE_TUNNEL_ID",
  "CLOUDFLARE_TUNNEL_REMOTE_CONFIG",
  "CLOUDFLARE_TUNNEL_LOCAL_CONFIG",
  "TUNNEL_NODE_HOSTNAME",
  "TUNNEL_BT_HOSTNAME",
  "TUNNEL_PORT_DOMAIN",
  "TUNNEL_PORT_HOST_PREFIX",
  "TUNNEL_PORT_HOST_TEMPLATE",
  "XRAY_PORT",
  "BT_PORT",
];

function shellQuote(value) {
  return `'${String(value).replace(/'/g, "'\\''")}'`;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

class CloudflareTunnelManager {
  constructor(options = {}) {
    this.execAsync = options.execAsync;
    this.logger = options.logger || console;
    this.cwd = options.cwd || process.cwd();
    this.env = options.env || process.env;
    this.cloudflaredPath =
      options.cloudflaredPath || path.join(this.cwd, "cloudflared-linux");
    this.stateFile = options.stateFile || path.join(this.cwd, "port-tunnels.json");
    this.settingsFile =
      options.settingsFile || path.join(this.cwd, "named-tunnel-settings.json");
    this.startScript =
      options.startScript || path.join(this.cwd, "start-named-tunnel.sh");
    this.syncPromise = null;
    this.lastError = "";
    this.lastSyncAt = "";
  }

  logPath() {
    return path.join(this.cwd, LOG_FILE);
  }

  appendLog(message) {
    const line = `[${new Date().toISOString()}] ${message}\n`;
    try {
      fs.mkdirSync(this.cwd, { recursive: true });
      fs.appendFileSync(this.logPath(), line, "utf8");
    } catch (error) {
      this.logger.error(`写入固定隧道日志失败: ${error.message}`);
    }
  }

  resolveStartScript() {
    if (fs.existsSync(this.startScript)) {
      return this.startScript;
    }
    const bundled = path.join(__dirname, "..", "start-named-tunnel.sh");
    return fs.existsSync(bundled) ? bundled : this.startScript;
  }

  getSettings() {
    return resolveTunnelSettings(this.getConfigEnv());
  }

  getConfigEnv() {
    return this.readStoredConfig();
  }

  readStoredConfig() {
    if (!fs.existsSync(this.settingsFile)) {
      return {};
    }
    try {
      const config = JSON.parse(fs.readFileSync(this.settingsFile, "utf8"));
      return config && typeof config === "object" ? config : {};
    } catch (error) {
      this.lastError = `读取固定隧道配置失败: ${error.message}`;
      return {};
    }
  }

  writeStoredConfig(config) {
    fs.mkdirSync(this.cwd, { recursive: true });
    fs.writeFileSync(this.settingsFile, `${JSON.stringify(config, null, 2)}\n`, "utf8");
  }

  getEffectiveEnv(baseEnv = this.env || process.env) {
    const stored = this.readStoredConfig();
    return { ...baseEnv, ...stored };
  }

  applyStoredConfigToProcessEnv(baseEnv = process.env) {
    const stored = this.readStoredConfig();
    for (const key of STORED_CONFIG_KEYS) {
      const value = String(stored[key] ?? "").trim();
      if (value) {
        baseEnv[key] = value;
      }
    }
    return baseEnv;
  }

  writeShellEnvFile() {
    const stored = this.readStoredConfig();
    const envPath = path.join(this.cwd, SHELL_ENV_FILE);
    if (!Object.keys(stored).length) {
      if (fs.existsSync(envPath)) {
        fs.unlinkSync(envPath);
      }
      return envPath;
    }

    const lines = [
      "# Generated by btblog from named-tunnel-settings.json",
      "# Do not edit manually; update via admin panel.",
      "",
    ];
    for (const key of STORED_CONFIG_KEYS) {
      const value = String(stored[key] ?? "").trim();
      if (value) {
        lines.push(`export ${key}=${shellQuote(value)}`);
      }
    }
    lines.push("");
    fs.mkdirSync(this.cwd, { recursive: true });
    fs.writeFileSync(envPath, lines.join("\n"), "utf8");
    return envPath;
  }

  syncRuntimeConfig() {
    this.applyStoredConfigToProcessEnv();
    return this.writeShellEnvFile();
  }

  sanitizeConfigInput(input = {}) {
    const current = this.readStoredConfig();
    const text = (name) => String(input[name] ?? "").trim();
    const keepSecret = (name) => {
      const value = text(name);
      return value || current[name] || "";
    };
    const keepText = (name, fallback = "") => {
      const value = text(name);
      return value || current[name] || fallback;
    };

    const config = {
      CLOUDFLARE_TUNNEL_TOKEN: keepSecret("CLOUDFLARE_TUNNEL_TOKEN"),
      CLOUDFLARE_API_TOKEN: keepSecret("CLOUDFLARE_API_TOKEN"),
      CLOUDFLARE_ACCOUNT_ID: keepText("CLOUDFLARE_ACCOUNT_ID"),
      CLOUDFLARE_TUNNEL_ID: keepText("CLOUDFLARE_TUNNEL_ID"),
      CLOUDFLARE_TUNNEL_REMOTE_CONFIG: "true",
      CLOUDFLARE_TUNNEL_LOCAL_CONFIG: "false",
      TUNNEL_NODE_HOSTNAME: keepText("TUNNEL_NODE_HOSTNAME"),
      TUNNEL_BT_HOSTNAME: keepText("TUNNEL_BT_HOSTNAME"),
      TUNNEL_PORT_DOMAIN: keepText("TUNNEL_PORT_DOMAIN"),
      TUNNEL_PORT_HOST_PREFIX: keepText("TUNNEL_PORT_HOST_PREFIX", "p"),
      TUNNEL_PORT_HOST_TEMPLATE: keepText("TUNNEL_PORT_HOST_TEMPLATE"),
      XRAY_PORT: keepText("XRAY_PORT", "10086"),
      BT_PORT: keepText("BT_PORT", "8888"),
    };

    return Object.fromEntries(
      Object.entries(config).filter(([, value]) => String(value || "").trim() !== "")
    );
  }

  configStatus() {
    const config = this.readStoredConfig();
    const settings = this.getSettings();
    return {
      configured: settings.enabled,
      settingsFile: this.settingsFile,
      hasTunnelToken: Boolean(config.CLOUDFLARE_TUNNEL_TOKEN),
      hasApiToken: Boolean(config.CLOUDFLARE_API_TOKEN),
      accountId: settings.accountId,
      tunnelId: settings.tunnelId,
      nodeHostname: settings.nodeHostname,
      btHostname: settings.btHostname,
      portDomain: settings.portDomain,
      portHostPrefix: settings.portHostPrefix,
      portHostTemplate: settings.portHostTemplate || "",
      xrayPort: settings.xrayPort,
      btPort: settings.btPort,
      remoteConfig: settings.useRemoteConfig,
    };
  }

  async saveConfig(input = {}) {
    const config = this.sanitizeConfigInput(input);
    this.writeStoredConfig(config);
    this.syncRuntimeConfig();
    this.lastError = "";
    this.appendLog("config saved");
    const routePublish = await this.publishRoutes("config-save");
    return {
      ...this.configStatus(),
      routePublish,
    };
  }

  async publishRoutes(reason = "manual") {
    const settings = this.getSettings();
    if (!settings.enabled) {
      return { skipped: true, reason: "固定隧道 token 未配置" };
    }
    if (!settings.useRemoteConfig) {
      return { skipped: true, reason: "本地配置模式不推送 Cloudflare API 路由" };
    }

    const artifacts = buildTunnelArtifacts({
      runtimeDir: this.cwd,
      env: this.getConfigEnv(),
      bindings: this.readPortBindings(),
    });
    const hasPublicRoute = artifacts.ingressRules.some((rule) => Boolean(rule.hostname));

    if (!settings.apiToken) {
      return { skipped: true, reason: "CLOUDFLARE_API_TOKEN 未配置" };
    }
    if (!settings.credentials) {
      return { skipped: true, reason: "未配置 Account ID / Tunnel ID，且 token 无法解析" };
    }
    if (!hasPublicRoute) {
      return { skipped: true, reason: "未配置节点域名、宝塔域名或端口域名" };
    }

    const result = await publishRemoteIngress(
      settings.credentials,
      artifacts.ingressRules,
      settings.apiToken
    );
    this.appendLog(`route publish ok (${reason})`);
    return result;
  }

  isEnabled() {
    return Boolean(this.getSettings().enabled);
  }

  readPortBindings() {
    if (!fs.existsSync(this.stateFile)) {
      return [];
    }
    try {
      const state = JSON.parse(fs.readFileSync(this.stateFile, "utf8"));
      const settings = this.getSettings();
      return Object.keys(state || {})
        .map((key) => Number.parseInt(key, 10))
        .filter((port) => Number.isInteger(port))
        .sort((a, b) => a - b)
        .map((port) => {
          const item = state[String(port)] || {};
          return {
            port,
            protocol: item.protocol || "http",
            hostname: item.hostname || buildPortHostname(port, settings),
          };
        });
    } catch {
      return [];
    }
  }

  buildNodeUrl() {
    const settings = this.getSettings();
    return settings.nodeHostname ? `https://${settings.nodeHostname}` : "";
  }

  buildBaotaUrl(panelPath = "") {
    const settings = this.getSettings();
    if (!settings.btHostname) {
      return "";
    }
    const suffix = panelPath && !panelPath.startsWith("/") ? `/${panelPath}` : panelPath || "";
    return `https://${settings.btHostname}${suffix}`;
  }

  buildLogSummary(status = null) {
    const current = status || {};
    const settings = this.getSettings();
    const lines = [
      "[固定隧道状态]",
      `enabled: ${this.isEnabled()}`,
      `running: ${Boolean(current.running)}`,
      `mode: ${settings.useRemoteConfig ? "token-env" : "local-config"}`,
      `node: ${settings.nodeHostname || "-"} -> 127.0.0.1:${settings.xrayPort}`,
      `bt: ${settings.btHostname || "-"} -> 127.0.0.1:${settings.btPort}`,
      `apiToken: ${settings.apiToken ? "已配置" : "未配置"}`,
      `lastSyncAt: ${this.lastSyncAt || "-"}`,
    ];
    if (this.lastError) {
      lines.push(`lastError: ${this.lastError}`);
    }
    if (current.needsPublicHostnames) {
      lines.push(
        "hint: 未设置 CLOUDFLARE_API_TOKEN 时，需要在 Cloudflare 控制台手动添加 Public Hostname"
      );
    }
    lines.push("", "===== named-tunnel.log =====", this.readLogTail(12000) || "暂无固定隧道日志");
    return lines.join("\n");
  }

  async ensureCloudflared() {
    if (fs.existsSync(this.cloudflaredPath)) {
      return this.cloudflaredPath;
    }

    const archScript =
      "case \"$(uname -m)\" in " +
      "x86_64|x64|amd64) echo amd64 ;; " +
      "aarch64|arm64) echo arm64 ;; " +
      "*) echo unsupported ;; esac";

    const { stdout } = await this.execAsync(archScript, {
      cwd: this.cwd,
      timeout: 5000,
      shell: "/bin/bash",
    });
    const arch = String(stdout || "").trim();
    if (arch !== "amd64" && arch !== "arm64") {
      throw new Error(`当前架构不支持 cloudflared: ${arch || "unknown"}`);
    }

    const url = `https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-${arch}`;
    const quotedBin = shellQuote(this.cloudflaredPath);
    this.appendLog(`downloading cloudflared (${arch})`);
    await this.execAsync(
      `curl -fsSL ${shellQuote(url)} -o ${quotedBin} && chmod +x ${quotedBin}`,
      {
        cwd: this.cwd,
        timeout: 120_000,
        maxBuffer: 1024 * 1024,
      }
    );

    if (!fs.existsSync(this.cloudflaredPath)) {
      throw new Error("cloudflared 下载失败");
    }
    return this.cloudflaredPath;
  }

  async isRunning() {
    const checks = [
      `pgrep -f ${shellQuote(PROCESS_NAME)} || true`,
      `pgrep -f ${shellQuote("cloudflared-linux tunnel run")} || true`,
      `pgrep -f ${shellQuote("cloudflared-linux tunnel .*run")} || true`,
      `pgrep -f ${shellQuote("./cloudflared-linux tunnel run")} || true`,
      `pgrep -f ${shellQuote("./cloudflared-linux tunnel .*run")} || true`,
    ];

    for (const command of checks) {
      const { stdout } = await this.execAsync(command, {
        cwd: this.cwd,
        timeout: 5000,
        maxBuffer: 1024 * 1024,
        shell: "/bin/bash",
      });
      if (String(stdout || "").trim()) {
        return true;
      }
    }
    return false;
  }

  async stop() {
    await this.execAsync(`pkill -9 -f ${shellQuote(PROCESS_NAME)} >/dev/null 2>&1 || true`, {
      cwd: this.cwd,
      timeout: 5000,
    });
    await this.execAsync(
      `pkill -9 -f ${shellQuote("cloudflared-linux tunnel run")} >/dev/null 2>&1 || true`,
      {
        cwd: this.cwd,
        timeout: 5000,
      }
    );
    await this.execAsync(
      `pkill -9 -f ${shellQuote("cloudflared-linux tunnel .*run")} >/dev/null 2>&1 || true`,
      {
        cwd: this.cwd,
        timeout: 5000,
      }
    );
  }

  readLogTail(limit = 4000) {
    const logPath = this.logPath();
    if (!fs.existsSync(logPath)) {
      return "";
    }
    try {
      return fs.readFileSync(logPath, "utf8").slice(-limit);
    } catch {
      return "";
    }
  }

  async startViaScript() {
    const scriptPath = this.resolveStartScript();
    if (!fs.existsSync(scriptPath)) {
      throw new Error(`未找到 start-named-tunnel.sh: ${scriptPath}`);
    }

    const quotedScript = shellQuote(scriptPath);
    await this.execAsync(`chmod +x ${quotedScript}`, {
      cwd: this.cwd,
      timeout: 10000,
    });

    const settings = this.getSettings();
    const childEnv = {
      ...this.getEffectiveEnv(process.env),
      CLOUDFLARE_TUNNEL_TOKEN: settings.token,
      TUNNEL_TOKEN: settings.token,
      ARGO_RUNTIME_DIR: this.cwd,
    };

    await this.execAsync(`bash ${quotedScript}`, {
      cwd: this.cwd,
      timeout: 30_000,
      maxBuffer: 1024 * 1024,
      env: childEnv,
      shell: "/bin/bash",
    });
  }

  async sync(reason = "manual") {
    if (!this.isEnabled()) {
      this.lastError = "CLOUDFLARE_TUNNEL_TOKEN 未设置或无效";
      return { enabled: false, running: false, reason: this.lastError };
    }

    if (this.syncPromise) {
      return this.syncPromise;
    }

    this.syncPromise = this._sync(reason).finally(() => {
      this.syncPromise = null;
    });
    return this.syncPromise;
  }

  async _sync(reason) {
    this.lastError = "";
    this.appendLog(`sync start (${reason})`);
    const settings = this.getSettings();
    const bindings = this.readPortBindings();
    const artifacts = buildTunnelArtifacts({
      runtimeDir: this.cwd,
      env: this.getConfigEnv(),
      bindings,
    });

    let routePublish = { skipped: true };
    if (settings.useRemoteConfig) {
      try {
        routePublish = await this.publishRoutes(reason);
        if (routePublish.skipped) {
          this.appendLog(`route publish skipped: ${routePublish.reason || "仅启动 connector"}`);
        } else {
          this.appendLog("route publish ok via Cloudflare API");
        }
      } catch (error) {
        routePublish = { skipped: false, error: error.message };
        this.appendLog(`route publish failed: ${error.message}`);
        this.logger.warn(`Cloudflare API 推送路由失败，仍尝试启动隧道: ${error.message}`);
      }
    }

    await this.ensureCloudflared();
    await this.startViaScript();

    const running = await this.isRunning();
    if (!running) {
      const tail = this.readLogTail(1200);
      this.lastError = `启动固定隧道失败${tail ? `: ${tail.trim()}` : ""}`;
      this.appendLog(this.lastError);
      throw new Error(this.lastError);
    }

    this.lastSyncAt = new Date().toISOString();
    this.appendLog(`sync ok (${reason})`);
    return {
      enabled: true,
      running,
      reason,
      nodeUrl: this.buildNodeUrl(),
      baotaUrl: this.buildBaotaUrl(),
      ingressRules: artifacts.ingressRules,
      routePublish,
      logTail: this.readLogTail(),
    };
  }

  async status() {
    const settings = this.getSettings();
    const running = await this.isRunning();
    return {
      enabled: this.isEnabled(),
      running,
      remoteConfig: settings.useRemoteConfig,
      nodeHostname: settings.nodeHostname,
      btHostname: settings.btHostname,
      xrayPort: settings.xrayPort,
      btPort: settings.btPort,
      nodeUrl: this.buildNodeUrl(),
      baotaUrl: this.buildBaotaUrl(),
      bindings: this.readPortBindings(),
      needsPublicHostnames: settings.useRemoteConfig && !settings.apiToken,
      lastError: this.lastError,
      lastSyncAt: this.lastSyncAt,
      logTail: this.readLogTail(),
    };
  }

  async ensureRunning(reason = "keepalive") {
    if (!this.isEnabled()) {
      return { started: false, enabled: false, reason: this.lastError || "token missing" };
    }
    if (await this.isRunning()) {
      return { started: false, running: true };
    }
    const result = await this.sync(reason);
    return { started: true, running: result.running, result };
  }
}

module.exports = {
  CloudflareTunnelManager,
  shellQuote,
  SHELL_ENV_FILE,
  STORED_CONFIG_KEYS,
};
